import { http } from './http'

export const api = {
  login: (data) => http.post('/auth/login', data),
  register: (data) => http.post('/auth/register', data),
  me: () => http.get('/auth/me'),
  updateProfile: (data) => http.put('/auth/profile', data),
  changePassword: (data) => http.put('/auth/password', data),
  books: (params) => http.get('/books', { params }),
  bookDetail: (id) => http.get(`/books/${id}`),
  saveBook: (data) => data.id ? http.put(`/books/${data.id}`, data) : http.post('/books', data),
  changeBookStatus: (id, status) => http.put(`/books/${id}/status`, null, { params: { status } }),
  deleteBook: (id) => http.delete(`/books/${id}`),
  categories: () => http.get('/categories'),
  createCategory: (data) => http.post('/categories', data),
  publishers: () => http.get('/publishers'),
  createPublisher: (data) => http.post('/publishers', data),
  tags: () => http.get('/tags'),
  createTag: (data) => http.post('/tags', data),
  announcements: () => http.get('/announcements'),
  createAnnouncement: (data) => http.post('/announcements', data),
  borrow: (data) => http.post('/borrows', data),
  myBorrows: (params) => http.get('/borrows/mine', { params }),
  allBorrows: (params) => http.get('/borrows', { params }),
  renew: (id) => http.put(`/borrows/${id}/renew`),
  returnBook: (id) => http.put(`/borrows/${id}/return`),
  markFinePaid: (id) => http.put(`/borrows/${id}/fine/paid`),
  stats: () => http.get('/admin/stats'),
  users: (params) => http.get('/admin/users', { params }),
  createUser: (data) => http.post('/admin/users', data),
  updateUser: (id, data) => http.put(`/admin/users/${id}`, data),
  disableUser: (id) => http.put(`/admin/users/${id}/disable`),
  feedback: (data) => http.post('/feedback', data),
  feedbackList: () => http.get('/feedback'),
  updateFeedback: (data) => http.put('/feedback', data),
  configs: () => http.get('/configs'),
  updateConfig: (data) => http.put('/configs', data),
  logs: () => http.get('/logs')
}
